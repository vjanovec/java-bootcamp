public class Kalkulacka {

    public int scitani(int firstNum, int secondNum) {
        return firstNum+secondNum;
    }

    public int odcitani(int firstNum, int secondNum) {
        return firstNum-secondNum;
    }

    public int nasobeni(int firstNum, int secondNum) {
        return firstNum*secondNum;
    }

    public double deleni(int firstNum, int secondNum) {
        return (double) firstNum/secondNum;
    }
}
