    public void loadListOfCars() {

        ArrayList<Car> cl = data.Cars.getCarsList();
        DefaultListModel model = new DefaultListModel();

        for (int i = 0; i < cl.size(); i++) {
            model.add(i, cl.get(i));
        }

        CarList.setModel(model);
        CarList.repaint();

    }

    public void loadServiceBook() {

        if (selectedIndex >= 0) {
            if (data.Cars.getCarsList().size() > selectedIndex) {
                List<String> book = data.Cars.getCarsList().get(selectedIndex).serviceBook.getServiceRecords();
                DefaultListModel model = new DefaultListModel();

                for (int i = 0; i < book.size(); i++) {
                    model.add(i, book.get(i));
                }
                BookList.setModel(model);
                BookList.repaint();
            }
        }

    }

