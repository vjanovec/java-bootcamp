    private void CarListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_CarListMouseClicked
        int index = CarList.getSelectedIndex();
        selectedIndex = index;
        //String s = (String) CarList.getSelectedValue();

        brandInput.setText(data.Cars.getCarsList().get(index).getZnacka());
        modelInput.setText(data.Cars.getCarsList().get(index).getModel());
        mileageInput.setText(String.valueOf(data.Cars.getCarsList().get(index).getNajeto()));
        consumptionInput.setText(String.valueOf(data.Cars.getCarsList().get(index).getSpotreba()));
        priceInput.setText(String.valueOf(data.Cars.getCarsList().get(index).getCena()));
        loadServiceBook();
    }//GEN-LAST:event_CarListMouseClicked


    private void saveBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_saveBtnMouseClicked
        // TODO add your handling code here:
        int index = selectedIndex;
        //System.out.println(index);
        data.Cars.getCarsList().get(index).setZnacka(brandInput.getText());
        data.Cars.getCarsList().get(index).setModel(modelInput.getText());
        data.Cars.getCarsList().get(index).setNajeto(Integer.parseInt(mileageInput.getText()));
        data.Cars.getCarsList().get(index).setSpotreba(Double.parseDouble(consumptionInput.getText()));
        data.Cars.getCarsList().get(index).setCena(Double.parseDouble(priceInput.getText()));
        loadListOfCars();
        loadServiceBook();

    }//GEN-LAST:event_saveBtnMouseClicked

    private void createBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_createBtnMouseClicked
        // TODO add your handling code here:
        Car car = null;
        try {
            car = new Car(brandInput.getText(), modelInput.getText(), Integer.parseInt(mileageInput.getText()), Double.parseDouble(consumptionInput.getText()), Double.parseDouble(priceInput.getText()));
            car.serviceBook = new ServiceBook(car);
            car.serviceBook.addRecord("Prvni rekord k "+brandInput.getText());
            car.serviceBook.addRecord("Druhy rekord k "+brandInput.getText());
        } catch (Exception e) {
            e.printStackTrace();
        }
        ArrayList<Car> cl = data.Cars.getCarsList();
        cl.add(car);
        System.out.println(cl);
        loadListOfCars();
        loadServiceBook();
    }//GEN-LAST:event_createBtnMouseClicked

